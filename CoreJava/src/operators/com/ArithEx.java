package operators.com;

public class ArithEx {

	public static void main(String[] args) {
		int a=10;
		int b=30;
		System.out.println("Addition of Values :"+(a+b));
		System.out.println("Substraction of Values :"+(a-b));
		
		System.out.println("Multiplication of Values :"+(a*b));
		
		System.out.println("Division of Values :"+(a/b));
		
		System.out.println("Modules of Values :"+(a%b));
		

	}

}
